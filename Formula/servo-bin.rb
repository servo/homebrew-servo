class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-09-05.tar.gz"
  version "2016.09.05"
  sha256 "68a21046949ca81dd84b2c647ec2c66f70a1f4649b0d48d8cddc8acd5f8052f4"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
